const createWarehouseEntry = (entry: Omit<WarehouseSchema, 'latLng' | 'title' | 'address'> & {
    lat: number;
    lng: number;
    title?: string;
    address?: any;
}): WarehouseSchema => {
    return {
        ...entry,
        latLng: `${entry.lat}, ${entry.lng}`,
        generalizedLocation: {
            lat: entry.lat,
            lng: entry.lng,
        }
    };
};


